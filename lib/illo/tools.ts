// 일로 웹(라이트) AI 도구 정의 — "패키지" 방식.
// ★ 원칙: 한 번 누르면 바로 쓸 수 있는 "완성 묶음"이 통째로 나온다(여러 결과물 동시 생성).
//   각 패키지는 여러 분야 최고 AI를 단계로 엮는다(리서치=Gemini, 추론·작성=Claude,
//   교차검토=다른 모델, 이미지/영상=fal.ai, 전송 등). 단일 AI로 충분한 건 두지 않는다.

export interface IlloTool {
  id: string;
  title: string;
  icon: string;
  desc: string;
  inputLabel: string;
  placeholder: string;
  multiline: boolean;
  aspects?: string[];        // 다중 선택 칩(유형/대상/톤/강조 등)
  defaultAspects?: string[];
  lengthOptions?: string[];  // 단일 선택(길이)
  defaultLength?: string;
  cta: string;
  buildPrompt: (input: string, picked: string[]) => string;
}

const joinPicked = (picked: string[]) => (picked.length ? `\n\n[옵션]\n- ${picked.join('\n- ')}` : '');
// 모든 패키지 공통 지시 — 결과물 묶음을 한 번에, 각 항목을 '■ 제목'으로 구분해 바로 쓰게.
const PKG = (intro: string, items: string[]) =>
  `${intro}\n아래 결과물을 한 번에 모두 작성하세요. 각 항목을 "■ 항목명" 헤더로 또렷이 구분하고, 설명 없이 바로 쓸 수 있게 완성도 높게 채우세요.\n\n[이 패키지에 포함]\n${items.map((s) => '■ ' + s).join('\n')}`;

